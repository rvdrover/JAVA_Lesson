class DataTypes
{
	public static void main(String[]args)
	{
		byte b=100;
		short s=-223;
		int i=123543;
        int  ic=-9876345;
        long l=1234567891;
        float f=12.25f;
        double d=12345.234;
        boolean bl1=true;
        boolean bl2=false;
        char c1=88;//ASCII value for X 8
        char c2='Y';
		System.out.println("Byte value "+b);
		System.out.println("Short value "+s);
		System.out.println("Int 1 value "+i);
		System.out.println("Int 2 value "+ic);
		System.out.println("Long value "+l);
		System.out.println("Float value "+f);
		System.out.println("Double value"+d);
		System.out.println("Boolean 1 value "+bl1);
		System.out.println("Boolean 2 value "+bl2);
		System.out.println("Char 1 value "+c1);
		System.out.println("Char 2 value "+c2);
	}
}